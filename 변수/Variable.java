package 변수;
public class Variable {
    public static void main(String[] args) {
        // 변수
        // 영어로는 Variable로 (문자나 숫자 같은) 데이터를 담는 컨테이너다.

        int a; // 선언
        // int = 데이터 형식(int = 숫자). a = 변수의 이름

        a = 1;
        System.out.println(a+1); // 2
        
        a = 2;
        System.out.println(a+1); // 3
    }
}
